function somar(a,b){
    return a + b
}

function subtrair(a,b){
    return a - b
}
function dividir(a,b){
    return a / b
}
function multiplicar(a,b){
    return a * b
}

console.log("Soma:", somar(1,2))
console.log("Subtração:", subtrair(1,2))
console.log("Divisão:", dividir(1,2))
console.log("Multiplicação:", multiplicar(1,2))